package ex_04_Operators;

public class ex_4_Relational_Operators {
    public static void main(String[] args) {
        // < Less Than
        // < =  -> Less than or equal to
        // > Greater
        // > = Greater or equal
        // == ->  Equal to (but checking)
        // != -> Not equal

        // All of them will result boolean output.

        int a = 5;
        int b = 30;
        boolean c = a > b; // // 10> 30
        System.out.println(c);

        int age_snehal = 28;
        int hrishi = 29;

        boolean result = hrishi >= age_snehal;
        System.out.println(result);

        // age_hrishi > age_snehal or age_hrishi = age_snehal


    }
}
