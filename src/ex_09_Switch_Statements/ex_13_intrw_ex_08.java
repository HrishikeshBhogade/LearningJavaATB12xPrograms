package ex_09_Switch_Statements;

public class ex_13_intrw_ex_08 {
    public static void main(String[] args) {

        char code = 'C';
        switch (code){
            default:
                System.out.println("Hellooooooo");
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
        }

    }
}
