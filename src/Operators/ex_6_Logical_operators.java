package Operators;

public class ex_6_Logical_operators {
    public static void main(String[] args) {
//            || OR Gate
//            && AND Gate
//            ! NOT GAte

        boolean a = true;
        System.out.println(!a);

        boolean b = true;
        System.out.println(!!b);

        boolean c= true || false;

        System.out.println(c);

        boolean c1= false && true;
        System.out.println(c1);


    }
}
